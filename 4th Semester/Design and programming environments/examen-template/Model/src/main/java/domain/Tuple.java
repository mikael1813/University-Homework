package domain;

public class Tuple {
    String cuv1;
    String cuv2;

    public Tuple(String cuv1, String cuv2) {
        this.cuv1 = cuv1;
        this.cuv2 = cuv2;
    }

    public String getCuv1() {
        return cuv1;
    }

    public void setCuv1(String cuv1) {
        this.cuv1 = cuv1;
    }

    public String getCuv2() {
        return cuv2;
    }

    public void setCuv2(String cuv2) {
        this.cuv2 = cuv2;
    }
}
